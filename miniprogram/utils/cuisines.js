/**
 * 根据 ID 获取菜系信息
 * @param {string} cuisineId - 菜系 ID
 * @returns {Object|null} 菜系信息
 */
const getCuisineById = (cuisineId) => {
  return CUISINES.find(c => c.id === cuisineId) || null;
};

/**
 * 获取所有菜系列表（精简版，用于导航展示）
 * @returns {Array} 菜系列表
 */
const getCuisineList = () => {
  return CUISINES.map(c => ({
    id: c.id,
    name: c.name,
    emoji: c.emoji,
    color: c.color,
    lightColor: c.lightColor,
    description: c.description,
    tags: c.tags,
    dishCount: c.representativeDishes.length,
  }));
};

/**
 * 根据食材关键词查找相关菜系
 * @param {string[]} ingredients - 食材列表
 * @returns {Array} 相关菜系列表（按匹配度排序）
 */
const findRelatedCuisines = (ingredients) => {
  if (!ingredients || ingredients.length === 0) return CUISINES.slice(0, 4);

  const scored = CUISINES.map(cuisine => {
    let score = 0;
    ingredients.forEach(ing => {
      if (cuisine.quickIngredients.some(qi => qi.includes(ing) || ing.includes(qi))) {
        score += 2;
      }
      if (cuisine.representativeDishes.some(dish =>
        dish.ingredients.some(di => di.includes(ing) || ing.includes(di))
      )) {
        score += 1;
      }
    });
    return { ...cuisine, matchScore: score };
  });

  return scored.sort((a, b) => b.matchScore - a.matchScore).slice(0, 4);
};

/**
 * 随机获取指定数量的菜系
 * @param {number} count - 数量
 * @returns {Array} 随机菜系列表
 */
const getRandomCuisines = (count = 4) => {
  const shuffled = [...CUISINES].sort(() => Math.random() - 0.5);
  return shuffled.slice(0, count);
};

/**
 * 获取菜系的代表菜（支持按难度筛选）
 * @param {string} cuisineId - 菜系 ID
 * @param {string} difficulty - 难度筛选（可选）
 * @returns {Array} 代表菜列表
 */
const getCuisineDishes = (cuisineId, difficulty = null) => {
  const cuisine = getCuisineById(cuisineId);
  if (!cuisine) return [];

  if (!difficulty) return cuisine.representativeDishes;

  return cuisine.representativeDishes.filter(d => d.difficulty === difficulty);
};

/**
 * 获取减脂餐列表
 * @param {number} limit - 返回数量（默认全部）
 * @returns {Array} 减脂餐列表
 */
const getFatLossMeals = (limit = null) => {
  if (limit) return FAT_LOSS_MEALS.slice(0, limit);
  return FAT_LOSS_MEALS;
};

/**
 * 获取孕妇营养餐列表
 * @param {string} trimester - 孕期阶段 early/mid/late（可选）
 * @param {number} limit - 返回数量（默认全部）
 * @returns {Array} 孕妇营养餐列表
 */
const getPregnancyMeals = (trimester = null, limit = null) => {
  let meals = PREGNANCY_MEALS;
  if (trimester) {
    meals = PREGNANCY_MEALS.filter(m => m.trimester.includes(trimester));
  }
  if (limit) return meals.slice(0, limit);
  return meals;
};

/**
 * 根据营养素获取孕妇餐
 * @param {string} nutrient - 营养素关键词
 * @returns {Array} 相关孕妇营养餐
 */
const getPregnancyMealsByNutrient = (nutrient) => {
  if (!nutrient) return PREGNANCY_MEALS;
  return PREGNANCY_MEALS.filter(m =>
    m.nutrients.some(n => n.includes(nutrient) || nutrient.includes(n))
  );
};

/**
 * 根据减脂目标获取减脂餐（按热量筛选）
 * @param {number} maxCalories - 最大热量
 * @returns {Array} 低热量减脂餐
 */
const getFatLossMealsByCalories = (maxCalories = 300) => {
  return FAT_LOSS_MEALS.filter(m => m.calories <= maxCalories);
};

/**
 * 根据当前时间获取每日推荐菜
 * 逻辑：用当天日期（0-based）作为索引，从所有菜肴池中确定性地选出一道
 * 池子包括：菜系代表菜 + 减脂餐 + 孕妇营养餐
 * @returns {Object} 推荐菜肴 { name, desc, cookTime, difficulty, ingredients, sourceType, sourceName, emoji, color, tags }
 */
const getDailyRecommendation = () => {
  // 构建所有菜肴池
  const allDishes = [];

  // 加入菜系代表菜
  CUISINES.forEach(cuisine => {
    cuisine.representativeDishes.forEach(dish => {
      allDishes.push({
        ...dish,
        sourceType: 'cuisine',
        sourceName: cuisine.name,
        cuisineId: cuisine.id,       // 用于跳转菜系详情页
        emoji: cuisine.emoji,
        color: cuisine.color,
        tags: dish.ingredients ? dish.ingredients.slice(0, 3) : [],
        category: 'cuisine',
      });
    });
  });

  // 加入减脂餐
  FAT_LOSS_MEALS.forEach(meal => {
    allDishes.push({
      name: meal.name,
      desc: meal.desc,
      cookTime: meal.cookTime,
      difficulty: meal.difficulty,
      ingredients: meal.ingredients,
      sourceType: 'fat_loss',
      sourceName: '减脂餐',
      emoji: '🥗',
      color: '#4CAF50',
      tags: meal.tags || [],
      calories: meal.calories,
      category: 'fat_loss',
    });
  });

  // 加入孕妇营养餐
  PREGNANCY_MEALS.forEach(meal => {
    allDishes.push({
      name: meal.name,
      desc: meal.desc,
      cookTime: meal.cookTime,
      difficulty: meal.difficulty,
      ingredients: meal.ingredients,
      sourceType: 'pregnancy',
      sourceName: '孕妇营养餐',
      emoji: '🤰',
      color: '#E91E63',
      tags: meal.tags || [],
      calories: meal.calories,
      category: 'pregnancy',
    });
  });

  if (allDishes.length === 0) return null;

  // 使用当天日期作为确定性索引
  const now = new Date();
  const dayOfYear = Math.floor((now - new Date(now.getFullYear(), 0, 0)) / (1000 * 60 * 60 * 24));
  const hourSlot = Math.floor(now.getHours() / 8); // 0-早, 1-午, 2-晚
  const index = (dayOfYear * 3 + hourSlot) % allDishes.length;

  return allDishes[index];
};

